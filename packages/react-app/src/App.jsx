const useProviderNetwork = provider => {
  const [network, setNetWork] = useState({});
  const getNetwork = async () => {
    const network = await provider.getNetwork();
    setNetWork(network);
  };
  useEffect(() => {
    if (provider) {
      getNetwork();
    }
  }, [provider]);

  return network;
};

